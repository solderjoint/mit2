#ifndef __UTIL_TYPEDEFS_H
#define __UTIL_TYPEDEFS_H

#include <stdint.h>

/* **************************************************** *
 *          TYPEDEFS INTEGER TYPE DEFINITIONS
 * **************************************************** */
typedef int8_t      int8;
typedef int16_t     int16;
typedef int32_t     int32;
typedef int64_t     int64;

typedef uint8_t     uint8;
typedef uint16_t    uint16;
typedef uint32_t    uint32;
typedef uint64_t    uint64;

/* **************************************************** *
 *          END OF TYPEDEFS HEADER DECLARATION
 * **************************************************** */
#endif //__UTIL_TYPEDEFS_H
